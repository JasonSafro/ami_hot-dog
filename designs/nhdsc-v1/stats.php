        <h3>Demographics</h3>
        <p>  Dinner sausage consumption is fairly uniform throughout various income levels, while lower income families consume the most breakfast sausage.</p>
        <p>  Larger families eat the most breakfast and dinner sausage, as do younger families, with sausage consumption leveling off considerably for senior citizens. Residents of the Deep South consume the most dinner sausage, followed by the Northeast, according to a survey conducted by the Council.</p>
        <p> Los Angeles led the way in sales of dinner sausage with dollar sales of $118 million. New York and Chicago were the second and third largest markets with sales of $108.8 million and $108.6 million, respectively.</p>
        <p> Sausage consumption also varies by season. Dinner sausage sales reportedly peak during the summer months, with dollar sales accounting for nearly one-third of annual sales. Breakfast sausage sales peak during holiday months from November through January.</p>

