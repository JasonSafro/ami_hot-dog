            <h3>Retail Sales</h3>
            <p>Retail sales in major markets are collected when products are scanned at the checkout counter. Scanners read bar codes on uniform-weight products, like a package of ten to the pound branded wieners. The Nielsen Company records data of this kind.</p>
            <p>According to data for the year 2013, more than 1 billion packages of hot dogs were sold at retail stores. That number represents almost $2.5 billion in retail sales.</p>

            <h3>Ballpark Consumption</h3>
            <p>According to the Council's 2014 survey of hot dog and sausage consumption at major league ballparks in the United States, ballparks are expected to sell 21,357,361 hot dogs this season. The baseball fans love of the hot dog is why all top ten hot dog consuming cities host MLB teams. This information is based on sales data for the previous year and expected attendance for the upcoming season.</p>
