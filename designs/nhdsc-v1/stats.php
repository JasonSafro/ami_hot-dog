            <h3>Trends and Facts</h3>
            <p> Sales of hot dogs remain strong throughout the United States. Sales remained steady at the retail level, and other venues continue to post high sales as well, including airports, restaurants and ballparks.</p>
            <p> Driving sales is the popularity of high protein foods and interest in natural and organic products. New products available are mostly brand favorites reformulated with a reduced fat content or increased protein content. New products in flavors such as honey and brown sugar, barbeque, Cajun, spicy and teriyaki have hit the market in the last year. According to consumer research, convenient packaging, preparation and usage information are high priorities.</p>
            <p>   Consumers with larger households made up of older children in the Midwest and south continue to be the highest consumers of refrigerated packaged meat products. Sixty percent, mostly older consumers, of people surveyed said they preferred all beef hot dogs. Younger consumers preferred other products such as pork and chicken.</p>
            <p> The vast majority of hot dogs are of the skinless variety, cooked inside a cellulose casing that is removed prior to packaging. Some hot dogs are produced in natural casings that are not removed, these feature the characteristic “snap" that many people enjoy. Most hot dogs are sold eight to a pound. Approximately 35 percent are offered in 10 to the pound packages.</p>
            <p>While the hot dog is a popular entree across the country, consumption does vary by region. According to sales data for 2013, New Yorkers spent more money on hot dogs in retail stores ($126.5 million) than any other market in the country. Residents of Los Angeles came in second with $93.5 million spent on hot dogs.</p>
            <p> The summer months between Memorial Day and Labor Day continue to make up the “hot dog season." Hot dog producers estimate that an average of 38 percent or $614 million of the total number of hot dogs are sold during this time. Ten percent of annual retail hot dog sales occur during July, which is designated as National Hot Dog Month.</p>
            <p>   Experts believe sales of the entire refrigerated processed meat category will continue to grow in the future.</p>
