    <div class="large-8 medium-8 columns">
        <h2>Sausage Glossary </h2>
        <dl class=""> <!-- start list-->
            <dt>Andouille | France</dt>
            <dd>In France the traditional andouille is composed primarily of the intestines and stomach. In the US the sausage is most often associated with Cajun cooking, where it is a coarse-grained smoked sausage made using pork, garlic, pepper, onions, wine, and seasonings. Traditional Andouille is heavily spiced and used as an ingredient where a smoked sausage version is more commonly available and consumed as is or on a bun.
                <em>Cooked, smoked sausage</em>
            </dd>
            <dt>Bangers | United Kingdom</dt>
            <dd> Sausage-like product prepared with meat and varying amounts of rusk or other cereals.
                <em>Fresh or pre-cooked sausage.</em>
            </dd>
            <dt>Berliner-Style | Germany</dt>
            <dd>Made of cured, coarsely ground pork and some mildly cured, finely chopped beef; contains no seasoning other than sugar and salt; available in rolls or packaged slices.
                <em>Cooked, smoked sausage</em>
            </dd>
            <dt>Blood and Tongue Sausage| Unknown Origin </dt>
            <dd>Cooked pork, beef or lamb and tongues are added to blood sausage mixtures. Pork snouts are also sometimes added.
                <em>Cooked sausage. </em>
            </dd>
            <dt>Blood Sausage |  Unknown Origin </dt>
            <dd>Diced, cooked fat pork, finely ground cooked meat, and gelatin-producing materials mixed with beef blood and spices.

                <em>Cooked sausage. </em>
            </dd>
            <dt>Bologna | Italy </dt>
            <dd>Made of cured beef and pork, finely ground, with seasonings similar to frankfurters; available in rings, rolls or slices of varying diameters; fully cooked and ready to serve.
                <em> Cooked, smoked sausage.</em>
            </dd>
            <dt> Boterhamworst | Netherlands</dt>
            <dd> Dutch-style sausage made of veal and pork, finely chopped and blended with coarsely chopped pork fat and seasonings.
                <em>Cooked, smoked sausage. </em>
            </dd>
            <dt>Bratwurst| Germany </dt>
            <dd> Pork or a pork and veal mixture; highly seasoned; made in links and available both fresh and fully cooked. Unique flavor is commonly derived from pepper, sage, and nutmeg.
                <em>Fresh or cooked, smoked sausage. </em>
            </dd>
            <dt>Braunschweiger | Germany </dt>
            <dd> Liver sausage which must contain at least 30% pork, beef or veal livers. Can be smoked after cooking or include smoked meat as ingredients.
                <em>Cooked sausage. </em>
            </dd>
            <dt>Cervelat | Switzerland </dt>
            <dd>General classification for mildly seasoned smoked, semi-dry sausages. Popularly termed “Summer Sausage”.
                <em>Semi-dry sausage. </em>
            </dd>
            <dt>Chorizo | Spain </dt>
            <dd>Sausage from Spanish origin made from meat coarsely cut; smoked; highly spiced and has a size similar to large frankfurters, one-inch links also made for sausage balls. Also is a term to denote any type of Spanish style sausage.
                <em>Fresh, cooked or dry/semi-dry sausage. </em>
            </dd>
            <dt>Frankfurters | Germany </dt>
            <dd> Combination of beef and pork or all beef which is cured, smoked and cooked; seasonings may include coriander, garlic, ground mustard, nutmeg, salt, sugar and white pepper; fully cooked but usually served hot; terms “frankfurter,” “wiener” and “hot dog” often used interchangeably; sizes range from big dinner frankfurters to small cocktail size; may be skinless or with natural casings.
                <em>Cooked, smoked sausage. </em>
            </dd>
            <dt>Frizzes | Italy </dt>
            <dd> Cured lean pork, chopped coarsely and a small quantity of cured lean beef; highly spiced. Some varieties made with hot spices, some with sweet spices.
                <em>Dry sausage. </em>
            </dd>
            <dt>Goetta | Germany </dt>
            <dd> A breakfast sausage primarily composed of ground meat (pork, or pork and beef), steel-cut oats and spices.
                <em>Partially cooked or pre-cooked sausage. </em>
            </dd>
            <dt>Kielbasa | Poland </dt>
            <dd> Coarsely ground lean pork with beef added; highly seasoned with garlic. Also known as a Polish sausage.
                <em> Cooked, smoked sausage. </em>
            </dd>
            <dt>Knackwurst | Germany </dt>
            <dd> Similar in ingredients to franks and bologna with additional garlic added for stronger flavor; made in large natural casings or in skinless styles; fully cooked, but usually served hot; also known as Knoblouch or Garlic Sausage.
                <em>Cooked, smoked sausage. </em>
            </dd>
            <dt>Linguica | Portugal </dt>
            <dd> Portuguese sausage made from coarsely ground pork, seasoned with garlic, paprika, cumin seeds and sometimes cinnamon; brined with vinegar pickling liquid before stuffing; smoked; also called Longanzia.
                <em>Uncooked sausage. </em>
            </dd>
            <dt>Liver Cheese or Liver Loaf Germany </dt>
            <dd> Ingredients and processing similar to liver sausage but with slight alteration to achieve a firmer texture more body for slicing. Wrapped with a thin layer of pork back fat. Molded in sandwich-size brick shape.
                <em>Cooked sausage. </em>
            </dd>
            <dt>Liver Sausage | Germany </dt>
            <dd> Liverwurst comes in many flavors that vary by region; sliceable or spreadable. Liverwurst must contain at least 30% pig, beef or veal livers. Other ingredients are pork, beef or veal trimmings, fat, and spices including allspice, thyme, ground mustard seed, or nutmeg.
                <em> Cooked sausage. </em>
            </dd>
            <dt>Lebanon Bologna | USA </dt>
            <dd> Semi-dry sausage that originated in Lebanon, Pennsylvania; made of coarsely chopped beef; heavily smoked; has a tart, tangy taste; dark surface appearance.
                <em>Semi-dry sausage. </em>
            </dd>
            <dt>Lola or Lolita | Italy </dt>
            <dd> Made of mildly seasoned pork; contains garlic.
                <em>Dry sausage. </em>
            </dd>
            <dt>Mettwurst | Germany </dt>
            <dd> Cured beef and pork, ground and lightly spiced with allspice, ginger, mustard and coriander; smooth, spreadable consistency; normally heavily smoked and sometimes must be cooked before serving.
                <em>Cooked or uncooked, smoked sausage </em>
            </dd>
            <dt>Mortadella | Italy </dt>
            <dd> Italian-style sausage composed of very finely chopped, cured pork and beef with added cubes of white fat; delicately spiced with garlic, nutmeg and coriander; stuffed into larger diameter casing and sliced.
                <em>Cooked, smoked sausage. </em>
            </dd>
            <dt>Pepperoni | Italy </dt>
            <dd> Pepperoni is a dry sausage that is characteristically firm, spicy, and bright red in color. Thinly sliced pepperoni is a popular pizza topping in American-style pizzerias. It is a variety of salami, usually made from cured pork and beef.
                <em>Dry sausage. </em>
            </dd>
            <dt>Pork Sausage | Various </dt>
            <dd> Made from fresh pork cuts and/or trimmings; seasoned with salt, black pepper, nutmeg, and rubbed sage, or other spices; sold in links, pre-formed patties or bulk.
                <em>Fresh sausage. </em>
            </dd>
            <dt>Salami | Italy </dt>
            <dd>General classification for dry sausage, sometimes highly seasoned, with characteristic fermented flavor. Usually made of beef and pork; seasoned with garlic, salt, pepper and sugar. Some are dried to achieve 30-40% moisture loss. Some are smoked and cooked.
                <em>Dry sausage. </em>
            </dd>
            <dt>Dry sausage. </dt>
            <dd> Refers to a style of pork sausage noted for being seasoned with fennel and/or anise as the primary seasoning. In Italy, however, there are a wide variety of sausages, many of which are quite different from the product commonly known as ‘Italian sausage’ in the United States. The two most common varieties marketed in US grocery stores as “Italian Sausage” are hot and sweet (or, depending on what region of the US, mild). The main difference between hot and sweet mild is the addition of hot red pepper flakes in the spice mix of
                the former.
                <em>Fresh, cooked or dry sausage. </em>
            </dd>
            <dt>Thuringer-Style Sausage Germany </dt>
            <dd>Sausages that would be made with spices and herbs found locally in the Thuringia region of Germany. Made principally of ground pork; may also include beef and veal; seasoning could included marjoram (stemless) and other herbs and spices; may be smoked or unsmoked.
                <em>Fresh or cooked sausage. </em>
            </dd>
            <dt>Vienna Sausage | Austria </dt>
            <dd> A similar version of a frankfurter or hot dog that is smaller in diameter and usually softer in texture. Ingredients similar to frankfurters. Term most often applied to small, open end sausages packed in cans of water. These are made into 80-foot lengths and cut into two-inch portions for canning. The name, vienna-style sausage, may also be used interchangeably with wiener or frankfurter.
                <em>Cooked or smoked sausage. </em>
            </dd>
            <dt> Weisswurst | Germany</dt>
            <dd> Means “white sausage;” made of veal
                and pork; mildly spiced; links are about four inches long and plump; very perishable; sometimes cooked.
                <em>Fresh or cooked sausage.</em>
            </dd>
            <dt>Wiener | Austria </dt>
            <dd> Both wieners and Vienna-style sausages take their names from the city of Vienna, Austria. Wiener-style, as originated, is sausage braided in groups of links. Vienna-style frankfurters are twisted into a chain of links. Terms are frequently used interchangeably with “frankfurter” or “hot dog” and formula may be the same.
                <em>Cooked, smoked or dried sausage.  </em>
            </dd>
        </dl>
