        <h2 class="gutter-bottom-lg text-center">A Guide to Sausage Varieties </h2>

        <p class="lead">Sausage, whether fresh, cooked or cured, remains a staple of the diet around the world.  Regions and cultures use various seasonings to make their sausages uniquely their own<p>

        <p class="lead">The sausages and meats are listed alphabetically. The group to which each sausage belongs is indicated next to the name of the sausage.</p>

