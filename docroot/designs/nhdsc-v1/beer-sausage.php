        <h2>Americans’ love beer and sausage.</h2>
       <p>Given Americans’ love for beer and sausage, the National Hot Dog and Sausage Council and the Beer Institute asked beer and food expert Peter LaFrance, author of Cooking and Eating with Beer, for his thoughts on pairing beer and sausage for optimum flavor.  Following are recommended combinations:</p>
        <p>Kolsch is light, refreshing (but dry) pale golden colored ale, which is usually served in tall, slender glasses.  It can legally be produced only in the Koln-Bonn metropolitan area in Germany. Kolsch is best paired with a mild sausage like Summer Sausage, also knownas  Cervelat, which is a general classification for mildly seasoned, mildly smoked, semi-dry sausages.</p>
        <p>Weissbier is one of the world’s oldest beer styles.  It is typically two parts wheat malt toone part barley malt, which creates a light-bodied beer that has about four to five percent alcohol.  Weissbier tastes best with Weisswurst, a fresh sausage of German origin.  Its name means “white sausage” and it is made of pork and veal, mildly spiced and sold in four inch, plump links.</p>
        <p>Stout is recognized as the heartiest of all beers.  Originated in Ireland, most traditional stouts are very rich, yet sharp and slightly bitter.  Stout is well-paired with a spicy Andouille, a strong-tasting sausage typically seasoned with oregano and thyme.</p>
        <p>Porter originally was a blend of stout and pale ale, but today, most porters are reddish in color, slightly sweet, very malty, but not bitter.  They are full-bodied, but not overpowering. Porter also is well-suited to Andouille or a spicy Italian sausage.</p>
        <p>Real Ales derive their carbonation from a final fermentation in the cask sent to the pub or in the bottle, much in the way champagne is carbonated.  Because of their living quality, real ales are sometimes cloudy and have a slightly yeasty character.  Real ales pair nicely with mortadella, mile salami and lightly smoked, sliced sausages</p>
        <p>Cream Ales are usually a blend of half lager and half ale and are a truly American creation, although more full-bodied and flavorful than some American beers.  Bratwurst and the all-American frankfurter complement cream ales. </p>
        <p>Oktoberfest style beer was traditionally the first beer of the brewing season in Germany.  It is amber-colored, sweet, extremely malty and slightly higher in alcohol content (about four to five percent).Oktoberfest beers pair well with mild wieners and ring bologna.</p>
        <p>Pilsner is the most popular lager in the world today, originating in Pilsen, Czech Republic.  Pilsners are a fairly dry style of beer with a strong malt flavor.  Pilsners pair well with grilled, mild sausages.</p>
        <p>Steam is recognized as the only classic American beer style and was invented in California during the 49erGold Rush days.  The beer has a deep amber color, a sharp flavor and a great deal of carbonation.  It is well-suited to Linguica, an uncooked, smoked sausage from Portugal made from coarsely ground pork butt, garlic, cumin and cinnamon.  Linguica is cured in vinegar pickling liquid before stuffing.  Steam also is suited to a Bratwurst with garlic.</p>        
        <p>Vienna beer originated in Vienna, Austria, and is a very sweet, lightly hopped, fairly light-bodied beer.  It is well-matched with mild Polish sausage, an uncooked, smoked sausage made of coarsely ground lean pork with beef added and mildly seasoned. </p>
        <p>Munchener Dunkel is one of two types of Munchener from Munich, Germany, and is full-bodied, malty and rather sweet.  Beers that are “Bavarian Style” are takeoffs of Munchener Dunkel.  Some of the new ,highly seasoned poultry sausages with basil, sun-dried tomatoes and other innovative flavors complement this beer nicely.</p>
        <p>Bock is a beer that was used to celebrate the end of the brewing season.  It is a dark sweet, heavy beer, much like the Muncheners, but stronger.  It is suited to Bockwurst, a fresh or cooked sausage made of veal (usually more veal than pork) and blended with milk, chives, eggs and chopped parsley. </p>


        
