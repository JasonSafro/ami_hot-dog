            <h4 class="gutter-top">Cooking with Beer:</h4>
            <p>In addition to consuming beer with sausage, fresh sausage also can be parboiled in beer and then fried or grilled until brown.  Sausages absorb the flavor of the beer; stronger beers impart stronger flavors.  Using a beer that is well-matched to a sausage promotes optimum flavor.  To parboil, place sausage links in a heavy skillet.  Add beer to cover sausage and par boil until sausage is grey throughout (about 10 to 15 minutes.) Onions also may be added for additional flavor.  The sausage then can be fried until nicely browned.</p>
        <p>Parboiled sausage also may be grilled slowly over coals, turning frequently until grey-brown throughout.</p>
